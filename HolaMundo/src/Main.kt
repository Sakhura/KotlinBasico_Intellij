/*Paso a paso
- fun main()
- Esto define una función llamada main, que es el punto de entrada de cualquier programa en Kotlin (similar a main() en Java o C++).
- Es el primer lugar que se ejecuta cuando corres el programa.
- val name = "Kotlin"
- Se declara una constante llamada name con el valor "Kotlin".
- Usar val significa que el valor no puede cambiar una vez asignado (es inmutable).
- println("Hola, " + name + "!")
- Combina (concatena) tres elementos en una sola cadena:
- "Hola, "
- el contenido de la variable name → "Kotlin"
- "!"
- El resultado final es "Hola, Kotlin!", que se muestra en la consola.

Resultado en pantalla
Hols, Kotlin!
* */
